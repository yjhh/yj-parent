package com.yj.service;

public class ServiceInfo {

  private String url;

  private String port;

  private int statue;

  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }

  public String getPort() {
    return port;
  }

  public void setPort(String port) {
    this.port = port;
  }

  public int getStatue() {
    return statue;
  }

  public void setStatue(int statue) {
    this.statue = statue;
  }
}
